#include "../libft.h"

int	ft_str_is_uppercase(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (0);
	while (str[i])
	{
		if (ft_isupper(str[i]) == 0)
			return (0);
		i++;
	}
	return (1);
}
