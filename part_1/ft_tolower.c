#include "../libft.h"

int	is_up(int c)
{
	if (c >= 65 && c <= 90)
		return (1);
	else
		return (0);
}

int	ft_tolower(int c)
{
	if (is_up(c) == 1)
		c += 32;
	return (c);
}
