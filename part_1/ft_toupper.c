#include "../libft.h"

int	is_low(int c)
{
	if (c >= 97 && c <= 122)
		return (1);
	else
		return (0);
}

int	ft_toupper(int c)
{
	if (is_low(c) == 1)
		c -= 32;
	return (c);
}
