#include "../libft.h"

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	if (!str || !str[0])
		return (NULL);
	while (str[i])
	{
		if (ft_isupper(str[i]) == 1)
			str[i] += 32;
		i++;
	}
	return (str);
}
